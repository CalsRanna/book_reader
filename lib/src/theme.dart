import 'package:flutter/material.dart';

class ReaderTheme {
  Color backgroundColor;
  EdgeInsets footerPadding;
  EdgeInsets headerPadding;
  Color textColor;
  double textFontSize;
  EdgeInsets textPadding;
  double titleFontSize;
  Color widgetColor;
  double widgetFontSize;

  ReaderTheme({
